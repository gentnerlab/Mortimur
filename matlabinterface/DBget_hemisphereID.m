function [ID, allData] = DBget_hemisphereID(conn, hemisphere)

curs = exec(conn, ['select hemisphereid from hemisphere where hemispherename = '  '''' hemisphere '''']);


if (isempty(curs.Message))
    data = fetch(curs);
    errormsg = '';
    close(curs);
else
    errormsg = curs.Message;
    fprintf([errormsg '\n']);
    error(['Error retreiving hemisphere data: ' errormsg]);
end

if (strcmp(data.Data{1}, 'No Data'))
    allData = {};
    ID = [];
else
    allData = data.Data;
    ID = cell2mat(data.Data(:,1));
end

end